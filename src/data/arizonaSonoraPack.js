function riskFor(zoneKey, co, id) {
  const base =
    co === "MX"
      ? { fl: 3, fN: "Moderate", wf: 2, wfN: "Low", dr: 4, drN: "High", hu: 4, huN: "High", ut: 7 }
      : { fl: 2, fN: "Zone X", wf: 3, wfN: "Moderate", dr: 3, drN: "Moderate", hu: 2, huN: "Low", ut: 6 };
  if (zoneKey === "phxMetro") {
    return { ...base, bo: 6, bN: "Urban edge", ut: 4, uR: "Urban heat island; ozone/haze summer; flash floods in washes." };
  }
  if (zoneKey === "coloradoRiver") {
    return { ...base, bo: 5, bN: "Suburban", dr: 5, drN: "Extreme", ut: 3, uR: "Extreme heat stress; Colorado River allocation risk." };
  }
  if (zoneKey === "mogollonRim" || zoneKey === "plateauNorth") {
    return { ...base, bo: 3, bN: "Rural Sky", wf: 4, wfN: "High", ut: 5, uR: "Wildfire + smoke; cold winter stress." };
  }
  if (zoneKey === "southeastSkyIsland") {
    return { ...base, bo: 3, bN: "Rural Sky", wf: 4, wfN: "High", ut: 7, uR: "Sky-island lightning fires; border corridor logistics." };
  }
  if (zoneKey === "sonoraBorder") {
    return { ...base, bo: 4, bN: "Suburban", ut: 6, uR: "Heat + dust storms; verify travel documents and insurance." };
  }
  if (zoneKey === "sonoraCoastLite") {
    return { ...base, bo: 4, bN: "Suburban", fl: 4, fN: "Coastal", hu: 5, huN: "Very High", ut: 8, uR: "Humid heat; tropical storm surge risk seasonal." };
  }
  return { ...base, bo: 4, bN: "Suburban", ut: 6, uR: "Typical Sonoran Basin-Range hazards; verify local flood maps." };
}

function makeCity(id, name, county, la, ln, pop, zoneKey, extra = {}) {
  const z = Z[zoneKey];
  const co = extra.co || "US";
  const r = extra.r || `${name}, ${county}${co === "US" ? ", AZ" : ""}`;
  const emoji = extra.em || z.em;
  const hasCustomNarrative = !!extra.bp;
  const bp = hasCustomNarrative ? extra.bp : buildRichBp(name, county, pop, zoneKey, co);
  const ac = extra.ac ?? buildRichAc(zoneKey, name);
  const ct = extra.ct ?? buildRichCt(name, county, pop, co);
  const ins = [...buildRichIns(zoneKey, name), ...(extra.ins || []), ...closingIns(co)];
  if (co === "MX") ins.unshift(MEX_PLATE_NOTE);
  const out = extra.out ?? buildOutlook(zoneKey, co, name);

  const o = {
    id,
    n: name,
    r,
    co,
    la,
    ln,
    ty: extra.ty || z.ty,
    nr: extra.nr || z.nr,
    eN: extra.eN ?? z.eN,
    eX: extra.eX ?? z.eX,
    sH: extra.sH ?? z.sH,
    sL: extra.sL ?? z.sL,
    wH: extra.wH ?? z.wH,
    wL: extra.wL ?? z.wL,
    ra: extra.ra ?? z.ra,
    hu: extra.hu ?? z.hu,
    aq: extra.aq ?? z.aq,
    aL: extra.aL || z.aL,
    so: extra.so || z.so,
    ph: extra.ph ?? z.ph,
    dr: extra.dr || z.dr,
    og: extra.og || z.og,
    vg: extra.vg || z.vg,
    gr: extra.gr || z.gr,
    la2: extra.la2 || z.la2,
    zo: extra.zo || z.zo,
    su: extra.su ?? z.su,
    gd: extra.gd || z.gd,
    em: emoji,
    bm: extra.bm || z.bm,
    vi: extra.vi || z.vi,
    tg: [...z.tg, ...(extra.tg || [])],
    ad: extra.ad ?? z.ad,
    bp,
    ac,
    ct,
    si: extra.si || [901, 20, 26],
    tl: extra.tl || `${name} — municipal microclimate & services snapshot.`,
    ins,
    out,
    img: `https://picsum.photos/seed/mc${id}/800/400`,
    ...(extra.wl ? { wl: extra.wl } : {}),
  };
  return { loc: o, rd: { ...riskFor(zoneKey, co, id), ...(extra.rd || {}) } };
}

