function riskFor(zoneKey, co, id) {
  const base =
    co === "MX"
      ? { fl: 3, fN: "Moderate", wf: 2, wfN: "Low", dr: 4, drN: "High", hu: 4, huN: "High", ut: 7 }
      : { fl: 2, fN: "Zone X", wf: 3, wfN: "Moderate", dr: 3, drN: "Moderate", hu: 2, huN: "Low", ut: 6 };
  if (zoneKey === "phxMetro") {
    return { ...base, bo: 6, bN: "Urban edge", ut: 4, uR: "Urban heat island; ozone/haze summer; flash floods in washes." };
  }
  if (zoneKey === "coloradoRiver") {
    return { ...base, bo: 5, bN: "Suburban", dr: 5, drN: "Extreme", ut: 3, uR: "Extreme heat stress; Colorado River allocation risk." };
  }
  if (zoneKey === "mogollonRim" || zoneKey === "plateauNorth") {
    return { ...base, bo: 3, bN: "Rural Sky", wf: 4, wfN: "High", ut: 5, uR: "Wildfire + smoke; cold winter stress." };
  }
  if (zoneKey === "southeastSkyIsland") {
    return { ...base, bo: 3, bN: "Rural Sky", wf: 4, wfN: "High", ut: 7, uR: "Sky-island lightning fires; border corridor logistics." };
  }
  if (zoneKey === "sonoraBorder") {
    return { ...base, bo: 4, bN: "Suburban", ut: 6, uR: "Heat + dust storms; verify travel documents and insurance." };
  }
  if (zoneKey === "sonoraCoastLite") {
    return { ...base, bo: 4, bN: "Suburban", fl: 4, fN: "Coastal", hu: 5, huN: "Very High", ut: 8, uR: "Humid heat; tropical storm surge risk seasonal." };
  }
  return { ...base, bo: 4, bN: "Suburban", ut: 6, uR: "Typical Sonoran Basin-Range hazards; verify local flood maps." };
}

